// Start by creating operator functions
function add(num1, num2) {
   let sum = num1 + num2;
   return sum;
} 

 function subtract(num1, num2) {
    let dif = num1 - num2;
    return dif;
}

function multiply(num1, num2) {
    let result = num1 * num2;
    return result;
}

function divide (num1, num2) {
    let quot = num1 / num2;
    return quot;
}